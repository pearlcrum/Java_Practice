package inheritance;

public class OverridingTest {

	public static void main(String[] args) {

		Customer customerLee = new Customer(10010,"이순신");

		customerLee.bonusPoint=1000;		
		
		
		VIPCustomor customerKim=new VIPCustomor(10020,"김유신");
		customerKim.bonusPoint=10000;
		
		int priceLee =customerLee.calcPrice(10000);
		int priceKim =customerKim.calcPrice(10000);
		
		System.out.println(customerLee.showCustomerInfo()+ "지불 금액은 " + priceLee);

		
		System.out.println(customerKim.showCustomerInfo()+ "지불 금액은 " + priceKim);
		
		Customer customerNo = new VIPCustomor(10030,"나몰라");
		customerNo.bonusPoint =10000;
		
		System.out.println(customerNo.showCustomerInfo()+ "지불 금액은 " + customerNo.calcPrice(10000));

		
		
	}

}
